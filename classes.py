
class calculator:

    def add(x, y):
        added = x + y
        print(added)

    def sub(x, y):
        sub = x - y
        print(sub)

    def mult(x, y):
        mult = x * y
        print(mult)

    def div(x, y):
        div = x / y
        print(div)

calculator.mult(3, 5)
calculator.add(5, 2)
calculator.sub(10, 9)
